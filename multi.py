def main():
    multi_to()
    divi_to()

def multi_to():
    tall = eval(input('Skriv inn et tall du vil multiplisere med to: '))
    tall_multi = tall * 2
    print (tall_multi)

def divi_to():
    tall = eval(input('Skriv inn et tall du vil dividere med to: '))
    tall_divi = tall / 2
    print (tall_divi)

main()
